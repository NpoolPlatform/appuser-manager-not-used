func CreateSet(c *ent.SubscriberCreate, info *npool.SubscriberReq) *ent.SubscriberCreate {
	if info.ID != nil {
		c.SetID(uuid.MustParse(info.GetID()))
	}
	if info.AppID != nil {
		c.SetAppID(uuid.MustParse(info.GetAppID()))
	}
	if info.EmailAddress != nil {
		c.SetEmailAddress(info.GetEmailAddress())
	}
	c.SetRegistered(false)
	return c
}
func Create(ctx context.Context, in *npool.SubscriberReq) (*ent.Subscriber, error) {
	var info *ent.Subscriber
	var err error

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "Create")
	defer span.End()
	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.Trace(span, in)

	err = db.WithClient(ctx, func(_ctx context.Context, cli *ent.Client) error {
		c := CreateSet(cli.Subscriber.Create(), in)
		info, err = c.Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}

	return info, nil
}

func CreateBulk(ctx context.Context, in []*npool.SubscriberReq) ([]*ent.Subscriber, error) {
	var err error
	rows := []*ent.Subscriber{}

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "CreateBulk")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.TraceMany(span, in)

	err = db.WithTx(ctx, func(_ctx context.Context, tx *ent.Tx) error {
		bulk := make([]*ent.SubscriberCreate, len(in))
		for i, info := range in {
			bulk[i] = CreateSet(tx.Subscriber.Create(), info)
		}
		rows, err = tx.Subscriber.CreateBulk(bulk...).Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}

	return rows, nil
}

